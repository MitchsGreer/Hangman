#include "play_menu_item.hpp"
#include "hangman.hpp"

////////////////////////////////////////////////////////////////////////////////
/// @brief The main entry point for playing the game of Hangman.
///
/// @return True if the game ended without error, False otherwise.
////////////////////////////////////////////////////////////////////////////////
bool PlayMenuItem::enter()
{
    Hangman game;
    return game.play();
}
